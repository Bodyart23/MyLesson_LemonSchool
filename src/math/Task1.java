public class Task1 {

    public static void main(String[] args) {

        double radian = Math.toRadians(30);
        double cos = Math.cos(radian);
        double sin = Math.sin(radian);
        System.out.println("Косинус 30 равен:" + cos);
        System.out.println("Синус 30 равен:" + sin);
    }
}
